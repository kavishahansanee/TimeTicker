    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try{
            String BrandName = txtBrand.getText();
            String WatchID = txtWatchID.getText();
            Price = Double.parseDouble(txtPrice.getText());
            Warranty = Integer.parseInt(txtWarranty.getText());

            objWatch = Watchcontrollerobj.addWatch(WatchID, BrandName, Price, Warranty);
            boolean result=Watchcontrollerobj.insertWatchToDB(objWatch);
            WatchArray[index] = objWatch;
            WatchList.add(WatchArray[index]);
            index++;
            if(result){
                JOptionPane.showMessageDialog(rootPane, "Are you sure you want to Add record? ","Alert",1);
                JOptionPane.showMessageDialog(rootPane, (index) + " Watch have been Added to Database Successfully", "Info", 1);
            }else{
               JOptionPane.showMessageDialog(rootPane, "Error adding to Database", "Error", 0); 
            }
 
        }catch(RuntimeException ex){
            if (txtBrand.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,"Brand name is Empty","Error",0);
            }
            if(txtWatchID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,"Watch ID is Empty","Error",0);
            }
            if(txtPrice.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,"Watch Price is Empty","Error",0);
            }
            if (txtWarranty.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"Watch Warranty is Empty","Error",0);

            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        try{
            String BrandName = txtBrand.getText();
            String WatchID = txtWatchID.getText();
            String prc = txtPrice.getText();
            String warnty = txtWarranty.getText();
            if (!prc.isEmpty()) {
               Price = Double.parseDouble(txtPrice.getText());
            }
            if (!warnty.isEmpty()) {
                 Warranty = Integer.parseInt(txtWarranty.getText());
            }
            
            objWatch = Watchcontrollerobj.deleteWatch(WatchID, BrandName, Price, Warranty);
            boolean result = Watchcontrollerobj.deleteWatchToDB(objWatch);
            WatchArray[index] = objWatch;
            WatchList.remove(WatchArray[index]);
            index++;
            if(result){
                JOptionPane.showMessageDialog(rootPane, "Are you sure you want to remove the record? ","Alert",1);
                JOptionPane.showMessageDialog(rootPane, " Information Removed from Database Successfully", "Info", 1);
            }else{
                JOptionPane.showMessageDialog(rootPane,"Error removing the Date", "Error", 0);
            }
            
        }catch(RuntimeException ex){
            JOptionPane.showMessageDialog(rootPane,"Can't Remove Data","Error",0);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

