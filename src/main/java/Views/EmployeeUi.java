    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
         try{
        String EmpID = txtEmpID.getText();
        String EmpName = txtEmpName.getText();
        String Address = txtAddress.getText();
        String ContactNo = txtContactNo.getText();        
        String EmpType = jcbEmpType.getSelectedItem().toString();
        Salary = Double.parseDouble(txtSalary.getText());
        

        objEmployee = objEmployeeController.AddEmployee(EmpID, EmpName, Address, ContactNo, EmpType, Salary);
        boolean result = objEmployeeController.insertEmployeeTODB(objEmployee);
        EmployeeArray[index] = objEmployee;
        EmployeeList.add(objEmployee);
        index++;
        if (result) {
            JOptionPane.showMessageDialog(rootPane, "Are you sure you want to Add record? ", "Alert", 1);
            JOptionPane.showMessageDialog(rootPane, (index) + " Supplier have been Added to Database Successfully", "Info", 1);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error adding to Database", "Error", 0);
        }
        
        }catch(RuntimeException ex){
             if (txtEmpName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,"Employee name is Empty","Error",0);
            }
            if(txtEmpID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,"Employee ID is Empty","Error",0);
            }
            if(txtAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,"Employee address is Empty","Error",0);
            }
            if (txtContactNo.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"Employee contact number is Empty","Error",0);

            }
            if (jcbEmpType.getSelectedItem().toString().isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"Please select Employee Type","Error",0);

            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

