    private void KeyListner(){
        txtContactNo.addKeyListener(new KeyAdapter() {
            @Override
            public void keyPressed(KeyEvent event) {
                
                if (event.getKeyChar() >= '0' && event.getKeyChar() <= '9' && txtContactNo.getText().length() < 10 || event.getKeyChar() == KeyEvent.VK_BACK_SPACE || event.getKeyChar() == KeyEvent.VK_DELETE || event.getKeyChar() == KeyEvent.VK_PLUS ) {
                    txtContactNo.setEditable(true);
                    
                } else {
                    txtContactNo.setEditable(false);                    
                }
            }
        });
    }
